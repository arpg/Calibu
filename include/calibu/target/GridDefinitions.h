  inline void LoadGridFromPreset( 
      const std::string& preset,//< input:
      Eigen::MatrixXi& grid,    //< output:
      double& grid_spacing,     //< output:
      double& large_dot_radius, //< output:
      double& small_dot_radius  //< output:
      )
  {
    if( preset == "small" ){ 
        grid = calibu::GWUSmallGrid();
        grid_spacing = 0.254 / 18;  // meters
        large_dot_radius = 0.00423;
        small_dot_radius = 0.00283;
    }
    else if (preset == "tiny")
      {
	grid = calibu::TinyGrid();
        grid_spacing = 0.02 ;  // meters
        large_dot_radius = 0.075;
        small_dot_radius = 0.05;
      }
    else if( preset == "letter" ){
        grid = calibu::LetterGrid();
        double paper_width = 8.5*0.0254;
        grid_spacing = paper_width / (grid.cols()+6);  // meters
        large_dot_radius = grid_spacing/3;
        small_dot_radius = 0.7*large_dot_radius;
    }
    else if( preset == "medium" ) {
        grid = calibu::MediumGrid();
        grid_spacing = 0.03156;  // meters
        large_dot_radius = 0.00889;
        small_dot_radius = 0.00635;
    }
    else if( preset == "large" ){
        grid = calibu::GoogleLargeGrid();
        grid_spacing = 0.03156;  // meters
        large_dot_radius = 0.00889;
        small_dot_radius = 0.00635;
    }
    else{
//      LOG(FATAL) << "Unknown grid preset " << preset;
    }
  }
